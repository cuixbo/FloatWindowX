package com.yingke.demo;

import android.util.Log;

public class FloatXLog {

    public static void d(String msg) {
        Log.d("FloatXLog", msg);
    }

    public static void e(String msg) {
        Log.e("FloatXLog", msg);
    }

    public static void w(String msg) {
        Log.w("FloatXLog", msg);
    }

}
